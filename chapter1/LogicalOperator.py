##### logical operator

### 1st : OR operator
# notes on OR : In Python, the ‘or’ operator is a logical operator that checks whether any of the conditions it is checking are True. 
           #    If at least one condition is True, the ‘or’ operator returns True. If all conditions are False, it returns False.


print(3 > 2 or 3 < 2)


### 2nd : AND operator
## notes on AND : in this operator that checks whether both the conditions true then it will print true otherwise it will print false
print("-------------")

print(3 > 2 and 4 < 5)
print(3 < 2 and 1 > 3)

print("-------------")

